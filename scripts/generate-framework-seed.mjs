// =============================================================================
// generate-framework-seed.mjs
// -----------------------------------------------------------------------------
// Reads src/data/initial-frameworks.json and writes supabase/seed_frameworks.sql
// as an idempotent INSERT that expands the JSON into the `frameworks` table.
//
// Why generate instead of hand-writing SQL?
//   The framework definitions contain large HTML/CSS blobs with quotes and
//   newlines. Embedding the raw JSON in a Postgres dollar-quoted block and
//   letting SQL expand it (jsonb_array_elements) avoids all manual escaping and
//   guarantees the seed matches the app's source data byte-for-byte.
//
// Usage:
//   node scripts/generate-framework-seed.mjs
// =============================================================================

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "..");

const jsonPath = join(repoRoot, "src", "data", "initial-frameworks.json");
const outPath = join(repoRoot, "supabase", "seed_frameworks.sql");

// Read the raw file text so the embedded JSON is identical to the source.
const rawJson = readFileSync(jsonPath, "utf8");

// Sanity check: make sure it parses and count the frameworks.
const parsed = JSON.parse(rawJson);
const count = Array.isArray(parsed.frameworks) ? parsed.frameworks.length : 0;

// Choose a dollar-quote tag that cannot appear in the JSON.
const tag = "$seed_json$";
if (rawJson.includes(tag)) {
  throw new Error(`JSON unexpectedly contains the dollar-quote tag ${tag}`);
}

const sql = `-- =============================================================================
-- SEED: default strategy frameworks ("skills")
-- =============================================================================
-- AUTO-GENERATED by scripts/generate-framework-seed.mjs — do not edit by hand.
-- Regenerate with:  node scripts/generate-framework-seed.mjs
--
-- Run this AFTER supabase/schema.sql. It loads the ${count} default frameworks
-- (SWOT, Porter's Five Forces, BMC, PESTLE, Ansoff, McKinsey 7S, Value Chain,
-- BCG, Balanced Scorecard, Blue Ocean) as active, playbook-visible skills.
--
-- Idempotent: re-running does nothing for frameworks that already exist
-- (matched on the unique "shortcut").
-- =============================================================================

with seed(doc) as (
  values (${tag}${rawJson}${tag}::jsonb)
)
insert into public.frameworks (
  title, shortcut, description, category, tags, when_to_use, icon,
  stages, departments, goal_alignment, ai_model, system_prompt,
  analysis_prompt, output_template, custom_css, response_schema,
  estimated_time, max_tokens, temperature, validate_json,
  status, show_in_playbooks
)
select
  f->>'title',
  f->>'shortcut',
  f->>'description',
  f->>'category',
  array(select jsonb_array_elements_text(coalesce(f->'tags', '[]'::jsonb))),
  f->>'when_to_use',
  f->>'icon',
  array(select jsonb_array_elements_text(coalesce(f->'stages', '[]'::jsonb))),
  array(select jsonb_array_elements_text(coalesce(f->'departments', '[]'::jsonb))),
  array(select jsonb_array_elements_text(coalesce(f->'goal_alignment', '[]'::jsonb))),
  coalesce(f->>'ai_model', 'google/gemini-2.5-flash'),
  f->>'system_prompt',
  f->>'analysis_prompt',
  f->>'output_template',
  f->>'custom_css',
  case when f ? 'response_schema' then f->'response_schema' else null end,
  coalesce((f->>'estimated_time')::int, 15),
  coalesce((f->>'max_tokens')::int, 4000),
  coalesce((f->>'temperature')::numeric, 0.7),
  coalesce((f->>'validate_json')::boolean, true),
  'active',
  true
from seed, jsonb_array_elements(seed.doc->'frameworks') as f
on conflict (shortcut) do nothing;
`;

writeFileSync(outPath, sql, "utf8");
console.log(`Wrote ${outPath} (${count} frameworks embedded).`);
